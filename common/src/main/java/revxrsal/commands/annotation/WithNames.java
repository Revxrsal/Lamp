package revxrsal.commands.annotation;

import revxrsal.commands.util.Either;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Represents an annotation that allows an {@link Either} parameter to
 * have multiple names depending on the type.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.PARAMETER)
@NotSender.ImpliesNotSender
public @interface WithNames {

    /**
     * The names. They will correspond to the types in the order they are
     * specified in {@link Either}.
     *
     * @return The names
     */
    String[] value();

}
